using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Methods_Assignment
{
   public class Math
    {
        public int Add(int data, int value)
        {
            return data + value;
        }

        public int Subtract(int data, int value)
        {
            return data - value;
        }

        public int Multiply(int data, int value)
        {
            return data * value;
        }
    }
}
